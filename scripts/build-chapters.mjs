/**
 * Build script: reads Markdown from content/pt/ and generates chapters.generated.ts
 * Run as part of: npm run build
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import matter from "gray-matter";
import { marked } from "marked";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CONTENT = path.join(__dirname, "../content/pt");
const OUT = path.join(__dirname, "../src/data/chapters.generated.ts");

const SLUGS = fs.readdirSync(CONTENT).filter((f) => f.endsWith(".md")).sort();

marked.setOptions({ gfm: true });

function parseBody(body) {
  const blocks = [];
  const parts = body.split(/\n\s*\*\*\*\s*\n/);
  for (let i = 0; i < parts.length; i++) {
    if (i > 0) blocks.push({ type: "scene-break" });
    const section = parts[i].trim();
    if (!section) continue;
    const paragraphs = section.split(/\n\n+/);
    for (const p of paragraphs) {
      const trimmed = p.trim();
      if (!trimmed) continue;
      const html = marked.parse(trimmed);
      const inner = String(html).replace(/^<p>|<\/p>\n?$/g, "").trim();
      blocks.push({ type: "paragraph", html: inner });
    }
  }
  return blocks;
}

function escapeForTs(str) {
  return str.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$/g, "\\$");
}

function run() {
  const chapters = [];
  for (let i = 0; i < SLUGS.length; i++) {
    const slug = SLUGS[i];
    const file = path.join(CONTENT, slug);
    if (!fs.existsSync(file)) {
      console.warn(`Missing: ${file}`);
      continue;
    }
    const raw = fs.readFileSync(file, "utf8");
    const { data, content } = matter(raw);
    const blocks = parseBody(content);
    chapters.push({
      id: i + 1,
      title: data.title || "",
      chapterNum: data.chapterNum || `Capítulo ${i + 1}`,
      blocks,
    });
  }
  const lines = [
    "// Auto-generated by scripts/build-chapters.mjs — do not edit",
    "",
    'import type { ContentBlock } from "./chapters-types";',
    "",
    "export type { ContentBlock } from \"./chapters-types\";",
    "",
    "export type Chapter = {",
    "  id: number;",
    "  chapterNum: string;",
    "  title: string;",
    "  subtitle: string;",
    "  content: ContentBlock[];",
    "};",
    "",
  ];
  function blocksToTs(blocks) {
    const arr = [];
    for (const block of blocks) {
      if (block.type === "scene-break") {
        arr.push("  { type: 'scene-break' },");
      } else {
        arr.push(`  { type: 'paragraph', html: \`${escapeForTs(block.html)}\` },`);
      }
    }
    return "[\n" + arr.join("\n") + "\n]";
  }

  for (const ch of chapters) {
    lines.push(`const chapter${ch.id}: ContentBlock[] = ${blocksToTs(ch.blocks)};`);
    lines.push("");
  }
  lines.push("export const chapters: Chapter[] = [");
  for (const ch of chapters) {
    lines.push("  {");
    lines.push(`    id: ${ch.id},`);
    lines.push(`    chapterNum: "${escapeForTs(ch.chapterNum)}",`);
    lines.push(`    title: "${escapeForTs(ch.title)}",`);
    lines.push(`    subtitle: "${escapeForTs(ch.title)}",`);
    lines.push(`    content: chapter${ch.id},`);
    lines.push("  },");
  }
  lines.push("];");
  lines.push("");
  fs.writeFileSync(OUT, lines.join("\n"), "utf8");
  console.log(`Generated ${OUT}`);
}

run();
